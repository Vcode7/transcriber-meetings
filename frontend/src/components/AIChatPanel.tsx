function ActionItemsRenderer({ items }: { items: string[] }) {
  if (!items || items.length === 0) return null

  type Section = {
    header: string
    items: string[]
  }

  let sections: Section[] = []

  // -------------------------------------------------------
  // Try parsing JSON first
  // -------------------------------------------------------
  if (
    items.length === 1 &&
    (items[0].trim().startsWith("{") || items[0].trim().startsWith("["))
  ) {
    try {
      const json = JSON.parse(items[0])

      if (json.general_action_items?.length) {
        sections.push({
          header: "General Action Items",
          items: json.general_action_items,
        })
      }

      if (json.speaker_action_items) {
        Object.entries(json.speaker_action_items).forEach(([speaker, value]) => {
          sections.push({
            header: speaker,
            items: value as string[],
          })
        })
      }
    } catch {
      // Ignore and continue with normal parsing
    }
  }

  // -------------------------------------------------------
  // Normal text parser
  // -------------------------------------------------------
  if (sections.length === 0) {
    let current: Section = {
      header: "General Action Items",
      items: [],
    }

    sections.push(current)

    for (const raw of items) {
      const line = raw.trim()

      if (!line) continue

      const cleaned = line
        .replace(/^#+\s*/, "")
        .replace(/:$/, "")

      const isBullet =
        /^[-*•]\s+/.test(line) ||
        /^\d+\.\s+/.test(line)

      if (!isBullet) {
        current = {
          header: cleaned,
          items: [],
        }
        sections.push(current)
        continue
      }

      current.items.push(
        line
          .replace(/^[-*•]\s*/, "")
          .replace(/^\d+\.\s*/, "")
          .trim()
      )
    }

    sections = sections.filter(s => s.items.length)
  }

  // -------------------------------------------------------
  // Render
  // -------------------------------------------------------

  return (
    <div style={{ display: "flex", flexDirection: "column", gap: "18px" }}>
      {sections.map((section, sectionIndex) => {
        const isGeneral = /general/i.test(section.header)

        return (
          <div key={sectionIndex}>
            <div
              style={{
                display: "flex",
                alignItems: "center",
                gap: "8px",
                marginBottom: "10px",
                paddingBottom: "6px",
                borderBottom: "1px solid hsl(var(--border) / .25)",

                fontSize: ".70rem",
                fontWeight: 700,
                textTransform: "uppercase",
                letterSpacing: ".08em",

                color: isGeneral
                  ? "hsl(var(--accent))"
                  : "hsl(var(--ink))",
              }}
            >
              {!isGeneral && (
                <span
                  style={{
                    width: 18,
                    height: 18,
                    borderRadius: "50%",
                    display: "flex",
                    alignItems: "center",
                    justifyContent: "center",

                    background: `hsl(${(sectionIndex * 137) % 360},70%,90%)`,
                    color: `hsl(${(sectionIndex * 137) % 360},70%,35%)`,

                    fontSize: ".62rem",
                    fontWeight: 700,
                  }}
                >
                  {section.header.charAt(0).toUpperCase()}
                </span>
              )}


              <MarkdownContent content={section.header} />
            </div>

            {section.items.map((item, i) => (
              <div
                key={i}
                style={{
                  display: "flex",
                  alignItems: "flex-start",
                  gap: "8px",
                  marginBottom: "8px",
                }}
              >
                <span
                  style={{
                    color: isGeneral
                      ? "hsl(var(--accent))"
                      : "hsl(var(--success))",
                    fontSize: ".9rem",
                    marginTop: "2px",
                    flexShrink: 0,
                  }}
                >
                  ☐
                </span>

                <div style={{ flex: 1 }}>
                  <MarkdownContent content={item} />
                </div>
              </div>
            ))}
          </div>
        )
      })}
    </div>
  )
}
