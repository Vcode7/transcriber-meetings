function CopyButton({ text }: { text: string }) {
  const [copied, setCopied] = useState(false)
  const copy = () => {
    navigator.clipboard.writeText(text)
    setCopied(true)
    setTimeout(() => setCopied(false), 1800)
  }
  return (
    <button
      onClick={copy}
      title="Copy"
      style={{
        background: 'none', border: 'none', cursor: 'pointer',
        color: 'hsl(var(--pencil))', padding: '3px 6px', borderRadius: '5px',
        display: 'flex', alignItems: 'center', gap: '4px',
        fontSize: '.68rem', fontFamily: 'Inter, sans-serif',
        transition: 'all .2s', opacity: 0.55,
      }}
      onMouseEnter={(e) => { const el = e.currentTarget as HTMLButtonElement; el.style.opacity = '1'; el.style.background = 'hsl(var(--muted))' }}
      onMouseLeave={(e) => { const el = e.currentTarget as HTMLButtonElement; el.style.opacity = '0.55'; el.style.background = 'none' }}
    >
      <span style={{ display: 'inline-block', transition: 'transform .2s', transform: copied ? 'rotateY(180deg)' : 'rotateY(0deg)' }}>
        {copied ? <Check size={11} style={{ color: 'hsl(var(--success))' }} /> : <Copy size={11} />}
      </span>
      {copied ? 'Copied!' : 'Copy'}
    </button>
  )
}

